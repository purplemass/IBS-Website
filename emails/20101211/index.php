		<td valign="top">
			<p style="margin-bottom: 1em; line-height: 1.3;">IBS&apos;s previous Fundraising Event (held in February 2010), was attended by more than 300 Iranian business leaders and members of the community and we fully expect that this year&apos;s event will receive an even greater degree of support from the numerous individuals, institutions and donors that have helped to drive the Project forward over the course of the past year.</p>
			<p style="margin-bottom: 1em; line-height: 1.3;">As a philanthropically-motivated project, we rely upon the cooperation of like-minded individuals wishing to help in the development of the next generation of business leaders in Iran, and would be grateful for your support and participation.</p>
			<p style="margin-bottom: 1em; line-height: 1.3;">
				<span style="display: block; color: #346794; font-size: 1.1em; font-weight: bold; margin-bottom: 2px;">Sponsorship Programme</span>
				We are currently seeking sponsors for the forthcoming 2011 Fundraising Event.
			</p>
			<p style="margin-bottom: 1em; line-height: 1.3;">Please find below details of our sponsorship packages designed for corporates and individuals. These packages have been formulated to provide participating sponsors with optimal exposure to a targeted global audience, as well as the opportunity to support the vision of bringing world-class business education to Iran.</p>
			<p style="margin-bottom: 1em; line-height: 1.3;">We thank you in advance for your consideration and look forward to discussing the Iranian Business School Project and our upcoming event with you at your earliest convenience. In the meantime, should you wish to find out more about the project, please visit our website at <a href="http://www.ibsproject.org" style="color: #1F2126; font-weight: bold; text-decoration: underline;">www.ibsproject.org</a> or contact us on <a href="mailto:events@ibsproject.org" style="color: #1F2126; font-weight: bold; text-decoration: underline;">events@ibsproject.org</a></p>
