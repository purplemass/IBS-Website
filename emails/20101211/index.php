			<p style="margin-bottom: 1em; line-height: 1.3;">The Iranian Business School (IBS) Project&apos;s Annual Fundraising Event will take place on <strong>4th February 2011</strong> at the Ballroom of <strong>The Dorchester Hotel</strong> in London and will provide an ideal opportunity to join together with other members of the Iranian community to help support the progress of an independent, world-class business school in Iran.</p>
			<p style="margin-bottom: 1em; line-height: 1.3;">Featuring a keynote speech by <strong>Dr. Eslambolchi, Chairman and CEO of consulting firm 2020 Venture Partners</strong>, and a publicly acclaimed global thought leader and &ldquo;technological genius&rdquo;, our event will also include an exclusive art auction of top works by renowned Iranian artists. Previous keynote speakers included Anousheh Ansari in 2009, the world&apos;s first female private space explorer, and Omid Kordestani in 2010, from the Office of Founders at Google.</p>
			<p style="margin-bottom: 1em; line-height: 1.3;">
				<span style="display: block; color: #346794; font-size: 1.1em; font-weight: bold; margin-bottom: 2px;">About the IBS Project</span>
				The IBS Project is a not-for-profit venture spearheaded by a group of philanthropists from the business community of the Iranian Diaspora and supported by leading private sector figures within Iran. Together, these individuals have been inspired to engage in a movement to bring opportunity to Iran, a nation brimming with unrealised potential. Educated,ambitious and enterprising,young Iranian citizens possess the same qualities that have placed thousands of their peers abroad amongst a recognised category of the international business elite. What differentiates them is access to the facilities that can nurture their talents, fortify their knowledge and provide the necessary proficiencies to participate in the global economy.
			</p>
			<span style="display: block; color: #346794; font-size: 1.1em; font-weight: bold;">Sponsorship Packages</span>
		</td>
